export function createI18n(locale = 'en') {
  const lang = translations[locale] || translations.en

  function t(key) {
    const parts = key.split('.')
    let val = lang
    for (const p of parts) {
      val = val?.[p]
    }
    return val || key
  }

  return { t, locale, translations }
}
